package vn.com.aris.dto;

public class AppleChartDTO {
	private String unitArray;
	private String nameApp;
	private String beginDate;
	private String endDate;
	private String developerProceeds;

	public String getUnitArray() {
		return unitArray;
	}

	public void setUnitArray(String unitArray) {
		this.unitArray = unitArray;
	}

	public String getNameApp() {
		return nameApp;
	}

	public void setNameApp(String nameApp) {
		this.nameApp = nameApp;
	}

	public String getBeginDate() {
		return beginDate;
	}

	public void setBeginDate(String beginDate) {
		this.beginDate = beginDate;
	}

	public String getEndDate() {
		return endDate;
	}

	public void setEndDate(String endDate) {
		this.endDate = endDate;
	}

	public String getDeveloperProceeds() {
		return developerProceeds;
	}

	public void setDeveloperProceeds(String developerProceeds) {
		this.developerProceeds = developerProceeds;
	}
}
